// Helper functions (لا تغيير هنا)
export const getRandomCategory = (mode) => {
  const categories = gameData[mode];
  return categories[Math.floor(Math.random() * categories.length)];
};

export const getRandomWord = (category) => {
  return category.words[Math.floor(Math.random() * category.words.length)];
};

export const getRandomPair = (category) => {
  return category.pairs[Math.floor(Math.random() * category.pairs.length)];
};

export const getAllCategories = (mode) => {
  return gameData[mode].map((cat) => cat.category);
};

export const getCategoryByName = (mode, categoryName) => {
  return gameData[mode].find((cat) => cat.category === categoryName);
};
